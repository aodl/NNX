import { readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const protoUrl =
  'https://raw.githubusercontent.com/dfinity/ic/master/rs/nns/governance/proto/ic_nns_governance/pb/v1/governance.proto';
const outPath = path.resolve('canisters/frontend/web/src/data/topics.generated.js');
const cachePath = path.resolve('tools/cache/governance.proto');
const noFallback = new Set([
  'TOPIC_UNSPECIFIED',
  'TOPIC_NEURON_MANAGEMENT',
  'TOPIC_GOVERNANCE',
  'TOPIC_SNS_AND_COMMUNITY_FUND',
]);
const labelAcronyms = new Set(['api', 'ic', 'kyc', 'os', 'sns']);

const fallbackProto = `enum Topic {
  TOPIC_UNSPECIFIED = 0;
  TOPIC_NEURON_MANAGEMENT = 1;
  TOPIC_EXCHANGE_RATE = 2;
  TOPIC_NETWORK_ECONOMICS = 3;
  TOPIC_GOVERNANCE = 4;
  TOPIC_NODE_ADMIN = 5;
  TOPIC_PARTICIPANT_MANAGEMENT = 6;
  TOPIC_SUBNET_MANAGEMENT = 7;
  TOPIC_APPLICATION_CANISTER_MANAGEMENT = 8;
  TOPIC_KYC = 9;
  TOPIC_NODE_PROVIDER_REWARDS = 10;
  reserved 11;
  TOPIC_IC_OS_VERSION_DEPLOYMENT = 12;
  TOPIC_IC_OS_VERSION_ELECTION = 13;
  TOPIC_SNS_AND_COMMUNITY_FUND = 14;
  TOPIC_API_BOUNDARY_NODE_MANAGEMENT = 15;
  TOPIC_SUBNET_RENTAL = 16;
  TOPIC_PROTOCOL_CANISTER_MANAGEMENT = 17;
  TOPIC_SERVICE_NERVOUS_SYSTEM_MANAGEMENT = 18;
}`;

export async function loadProto({
  fetchImpl = fetch,
  protoUrlOverride = protoUrl,
  cachePathOverride = cachePath,
  allowEmbedded = process.env.NNX_ALLOW_EMBEDDED_TOPIC_FALLBACK === '1',
} = {}) {
  try {
    const response = await fetchImpl(protoUrlOverride);
    if (!response.ok) throw new Error(`${response.status} ${response.statusText}`);
    return await response.text();
  } catch (error) {
    try {
      const cachedProto = await readFile(cachePathOverride, 'utf8');
      console.warn(
        `Warning: could not fetch upstream governance.proto; using pinned cache at ${path.relative(process.cwd(), cachePathOverride)}: ${error.message}`,
      );
      return cachedProto;
    } catch (cacheError) {
      if (allowEmbedded) {
        console.warn(
          `Warning: could not fetch upstream governance.proto or read pinned cache; using embedded fallback because NNX_ALLOW_EMBEDDED_TOPIC_FALLBACK=1: ${error.message}; cache error: ${cacheError.message}`,
        );
        return fallbackProto;
      }

      throw new Error(
          `Could not fetch upstream governance.proto and no pinned cache was available at ${path.relative(
            process.cwd(),
            cachePathOverride,
        )}. Set NNX_ALLOW_EMBEDDED_TOPIC_FALLBACK=1 to use the embedded emergency fallback. Fetch error: ${error.message}; cache error: ${cacheError.message}`,
      );
    }
  }
}

export function parseTopicEnum(proto) {
  const match = proto.match(/enum\s+Topic\s*\{([\s\S]*?)\n\}/);
  if (!match) throw new Error('Could not find enum Topic in governance.proto');

  return [...match[1].matchAll(/^\s*(TOPIC_[A-Z0-9_]+)\s*=\s*(\d+);/gm)]
    .map(([, protoName, id]) => ({ protoName, id: Number(id) }))
    .filter((topic) => topic.protoName !== 'TOPIC_UNSPECIFIED');
}

function toKey(protoName) {
  return protoName
    .replace(/^TOPIC_/, '')
    .toLowerCase()
    .split('_')
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join('');
}

function toLabel(protoName) {
  return protoName
    .replace(/^TOPIC_/, '')
    .toLowerCase()
    .split('_')
    .map((part) =>
      labelAcronyms.has(part) ? part.toUpperCase() : part.charAt(0).toUpperCase() + part.slice(1),
    )
    .join(' ')
    .replace('And', '&');
}

export async function main() {
  const topics = parseTopicEnum(await loadProto());
  const body = topics
    .map((topic) => `  {
    id: ${topic.id},
    protoName: '${topic.protoName}',
    key: '${toKey(topic.protoName)}',
    label: '${toLabel(topic.protoName)}',
    fallback: ${!noFallback.has(topic.protoName)},
  }`)
    .join(',\n');

  await writeFile(
    outPath,
    `// Generated by tools/scripts/generate-nns-topics.mjs.
// Do not edit manually.

export const TOPIC_UNSPECIFIED = 0;

export const NNS_TOPICS = Object.freeze([
${body}
]);
`,
  );
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  try {
    await main();
  } catch (error) {
    console.error(`generate-nns-topics failed: ${error.message}`);
    process.exitCode = 1;
  }
}
